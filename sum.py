def sum_two_number(
    a:float,
    b:float = 5
) -> float:
    """ Sum two values.
    
    Take two variabels and sum.
    
    Args
        a (float): First number.
        b (float, optional): Second number. Defaults to 5.
        
    Returns:
        float: Sum of both arguments
    """
          
    number = a + b
    
    return number